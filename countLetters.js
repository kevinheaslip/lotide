// accepts a sentence as a string and returns a count of each of the letters in the string
const countLetters = function(sentence) {
  const results = {};
  for (letter of sentence) {
    if (letter !== " ") {
      if (results[letter]) {
        results[letter] += 1;
      } else {
        results[letter] = 1;
      }
    }
  }
  return results;
};

