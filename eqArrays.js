const eqArrays = function(arr1, arr2) {
  let eqElements = false;
  if (arr1.length === arr2.length) {
    for (let i = 0; i < arr1.length; i++) {
      if (arr1[i] === arr2[i]) {
        eqElements = true;
      } else {
        eqElements = false;
      }
    }
  }
  return eqElements;
};

